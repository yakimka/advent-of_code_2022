from __future__ import annotations

import os

import pytest

INPUT_TXT = os.path.join(os.path.dirname(__file__), 'input.txt')


def noop():
    yield None


def addx(x_val: int):
    yield None
    yield x_val


def pipeline(s: str):
    for line in s.splitlines():
        if line == "noop":
            yield from noop()
        if line.startswith("addx "):
            yield from addx(int(line[5:]))


def compute(s: str) -> int:
    x = 1
    answer = 0
    signal_cycles = list(range(20, 221, 40))
    for i, val in enumerate(pipeline(s), start=1):
        if i in signal_cycles:
            answer += i * x
        if val is not None:
            x += val

    return answer


